// Suspend marks the account as suspended.
func (m *Manager) Suspend(ctx context.Context, id string, reason string) (*models.Account, error) {
	ra, err := m.repo.GetByID(ctx, id)
	if err != nil {
		return nil, fmt.Errorf("account.Suspend(%s): %w", id, err)
	}

	now := time.Now()
	ra.Status = string(models.AccountStatusSuspended)
	ra.IsSuspended = true
	ra.SuspensionReason = reason
	ra.SuspendedAt = &now  // FIX: ra.SuspendedAt is *time.Time — take address of now
	ra.UpdatedAt = now     // ra.UpdatedAt is time.Time — plain assignment

	if err := m.repo.Update(ctx, ra); err != nil {
		return nil, fmt.Errorf("account.Suspend(%s): persist: %w", id, err)
	}
	return toModel(ra), nil
}

// Activate re-enables a suspended or inactive account.
func (m *Manager) Activate(ctx context.Context, id string) (*models.Account, error) {
	ra, err := m.repo.GetByID(ctx, id)
	if err != nil {
		return nil, fmt.Errorf("account.Activate(%s): %w", id, err)
	}

	ra.Status = string(models.AccountStatusActive)
	ra.IsSuspended = false
	ra.SuspensionReason = ""
	ra.SuspendedAt = nil   // FIX: *time.Time — clear by setting nil
	ra.IsActive = true
	ra.UpdatedAt = time.Now()

	if err := m.repo.Update(ctx, ra); err != nil {
		return nil, fmt.Errorf("account.Activate(%s): persist: %w", id, err)
	}
	return toModel(ra), nil
}

// ─── Stats & Health ──────────────────────────────────────────────────────────

// GetStats derives sending statistics from the live account row.
func (m *Manager) GetStats(ctx context.Context, id string) (*Stats, error) {
	ra, err := m.repo.GetByID(ctx, id)
	if err != nil {
		return nil, fmt.Errorf("account.GetStats(%s): %w", id, err)
	}

	dailyRemaining := clampMin(ra.DailyLimit-ra.SentToday, 0)
	rotRemaining := clampMin(ra.RotationLimit-ra.SentThisRotation, 0)

	return &Stats{
		ID:                  ra.ID,         // string after Fix A
		Email:               ra.Email,
		TotalSent:           int(ra.TotalSent),
		TotalFailed:         int(ra.TotalFailed),
		SuccessRate:         ra.SuccessRate,
		SentToday:           ra.SentToday,
		HealthScore:         ra.HealthScore * 100,
		ConsecutiveFailures: ra.ConsecutiveFailures,
		LastUsedAt:          ra.LastUsedAt, // FIX: both are *time.Time — direct assignment, no &
		DailyLimitRemaining: dailyRemaining,
		RotationRemaining:   rotRemaining,
	}, nil
}

// GetHealth returns the health score (0–100) and a status label.
func (m *Manager) GetHealth(ctx context.Context, id string) (*Health, error) {
	ra, err := m.repo.GetByID(ctx, id)
	if err != nil {
		return nil, fmt.Errorf("account.GetHealth(%s): %w", id, err)
	}
	score := ra.HealthScore * 100
	return &Health{Score: score, Status: healthStatusLabel(score)}, nil
}

