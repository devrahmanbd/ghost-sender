// toModel maps the flat repository.Account to the richer models.Account.
// Key invariant: DB healthscore is 0–1; all exported types use 0–100.
func toModel(ra *repository.Account) *models.Account {
	acc := &models.Account{
		ID:        ra.ID, // string after Fix A
		Email:     ra.Email,
		Name:      ra.Name,
		Provider:  models.Provider(ra.Provider),
		Status:    models.AccountStatus(ra.Status),
		Priority:  ra.Priority,
		CreatedAt: ra.CreatedAt,
		UpdatedAt: ra.UpdatedAt,
		CreatedBy: ra.CreatedBy,
		UpdatedBy: ra.UpdatedBy,
		Limits: models.AccountLimits{
			DailyLimit:        ra.DailyLimit,
			RotationLimit:     ra.RotationLimit,
			SentToday:         int64(ra.SentToday),
			SentSinceRotation: int64(ra.SentThisRotation),
			RemainingDaily:    int64(clampMin(ra.DailyLimit-ra.SentToday, 0)),
		},
		Stats: models.AccountStats{
			TotalSent:   ra.TotalSent,
			TotalFailed: ra.TotalFailed,
		},
		HealthMetrics: models.AccountHealth{
			HealthScore:         ra.HealthScore * 100, // 0–1 → 0–100
			IsHealthy:           ra.HealthScore >= 0.7,
			ConsecutiveFailures: ra.ConsecutiveFailures,
			SuccessRate:         ra.SuccessRate,
		},
		// FIX: ra fields are *time.Time — assign directly, never take &
		LastUsedAt:    ra.LastUsedAt,
		CooldownUntil: ra.CooldownUntil,
	}

	// SMTP — only populate when a host is actually configured
	if ra.SMTPHost != "" {
		acc.SMTPConfig = &models.SMTPConfig{
			Host:   ra.SMTPHost,
			Port:   ra.SMTPPort,
			UseTLS: ra.SMTPUseTLS,
			UseSSL: ra.SMTPUseSSL,
		}
	}
if len(ra.EncryptedPassword) > 0 || ra.OAuthToken != "" { 
		acc.Credentials = &models.AccountCredentials{
			Password:     string(ra.EncryptedPassword),
			AccessToken:  ra.OAuthToken,
			RefreshToken: ra.OAuthRefreshToken,
			IsEncrypted:  len(ra.EncryptedPassword) > 0,
		}
		if ra.OAuthExpiry != nil {
			acc.Credentials.TokenExpiry = ra.OAuthExpiry
		}
	}

	// Suspension — only populate when actually suspended
	if ra.IsSuspended {
		// FIX: models.SuspensionInfo.SuspendedAt is time.Time but ra.SuspendedAt
		//      is *time.Time — dereference safely
		var suspendedAt time.Time
		if ra.SuspendedAt != nil {
			suspendedAt = *ra.SuspendedAt
		}
		acc.SuspensionInfo = &models.SuspensionInfo{
			SuspendedAt:      suspendedAt,
			SuspensionReason: ra.SuspensionReason,
			IsAutoSuspended:  ra.AutoSuspend,
			CanResume:        true,
		}
	}

	return acc
}

// healthStatusLabel converts a 0–100 score to a label.
func healthStatusLabel(score float64) string {
	switch {
	case score >= 90:
		return "healthy"
	case score >= 70:
		return "warning"
	default:
		return "unhealthy"
	}
}

// clampMin returns v unless v < minimum, in which case it returns minimum.
func clampMin(v, minimum int) int {
	if v < minimum {
		return minimum
	}
	return v
}
