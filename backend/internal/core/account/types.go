// Create inserts a new account. UUID is generated by the DB (RETURNING id).
func (m *Manager) Create(ctx context.Context, req *CreateRequest) (*models.Account, error) {
	encPass := req.Password
	if req.AppPassword != "" {
		encPass = req.AppPassword
	}

	ra := &repository.Account{
		Email:             req.Email,
		Name:              req.SenderName,
		Provider:          req.Provider,
		Status:            string(models.AccountStatusActive),
		EncryptedPassword: []byte(encPass),
		OAuthToken:        req.OAuthToken,
		SMTPHost:          req.SMTPHost,
		SMTPPort:          req.SMTPPort,
		SMTPUseTLS:        req.UseTLS,
		SMTPUseSSL:        req.UseSSL,
		DailyLimit:        req.DailyLimit,
		RotationLimit:     req.RotationLimit,
		HealthScore:       1.0,
		IsActive:          true,
		Config:            req.Config,
	}
	if req.ProxyID != nil && *req.ProxyID != "" {
		ra.ProxyID = *req.ProxyID
		ra.UseProxy = true
	}

	if err := m.repo.Create(ctx, ra); err != nil {
		return nil, fmt.Errorf("account.Create: %w", err)
	}
	return toModel(ra), nil
}

// List returns accounts matching opts together with the total row count.
func (m *Manager) List(ctx context.Context, opts *ListOptions) ([]*models.Account, int, error) {
	filter := repository.AccountFilter{
		SortBy:    opts.SortBy,
		SortOrder: opts.SortOrder,
	}
	if opts.Provider != "" {
		filter.Provider = []string{opts.Provider}
	}
	if opts.Status != "" {
		filter.Status = []string{opts.Status}
	}
	filter.IsSuspended = opts.IsSuspended
	if opts.PageSize > 0 {
		filter.Limit = opts.PageSize
		if opts.Page > 1 {
			filter.Offset = (opts.Page - 1) * opts.PageSize
		}
	}

	// FIX: repo.List takes *AccountFilter — pass address, not value
	ras, total, err := m.repo.List(ctx, &filter)
	if err != nil {
		return nil, 0, fmt.Errorf("account.List: %w", err)
	}

	accounts := make([]*models.Account, len(ras))
	for i, ra := range ras {
		accounts[i] = toModel(ra)
	}
	return accounts, total, nil
}

// GetByID fetches a single account by its UUID string.
func (m *Manager) GetByID(ctx context.Context, id string) (*models.Account, error) {
	ra, err := m.repo.GetByID(ctx, id) // repo now accepts string after Fix A
	if err != nil {
		return nil, fmt.Errorf("account.GetByID(%s): %w", id, err)
	}
	return toModel(ra), nil
}

func (m *Manager) Update(ctx context.Context, id string, req UpdateRequest) (*models.Account, error) {
    ra, err := m.repo.GetByID(ctx, id)
    if err != nil {
        return nil, fmt.Errorf("account.Update(%s): fetch: %w", id, err)
    }

    if req.Email != nil {
        ra.Email = *req.Email          // ✅ dereference
    }
    if req.Password != nil && *req.Password != "" {
        ra.EncryptedPassword = []byte(*req.Password)
    }
    if req.AppPassword != nil && *req.AppPassword != "" {
        ra.EncryptedPassword = []byte(*req.AppPassword) // app password takes precedence
    }
    if req.SenderName != nil {
        ra.Name = *req.SenderName
    }
    if req.DailyLimit != nil {
        ra.DailyLimit = *req.DailyLimit
    }
    if req.RotationLimit != nil {
        ra.RotationLimit = *req.RotationLimit
    }
    if req.SMTPHost != nil {
        ra.SMTPHost = *req.SMTPHost
    }
    if req.SMTPPort != nil {
        ra.SMTPPort = *req.SMTPPort
    }
    if req.UseSSL != nil {
        ra.SMTPUseSSL = *req.UseSSL
    }
    if req.UseTLS != nil {
        ra.SMTPUseTLS = *req.UseTLS
    }
    if req.ProxyID != nil {
        ra.ProxyID = *req.ProxyID
        ra.UseProxy = *req.ProxyID != ""
    }
    if req.Config != nil {
        ra.Config = req.Config
    }

    if err := m.repo.Update(ctx, ra); err != nil {
        return nil, fmt.Errorf("account.Update(%s): persist: %w", id, err)
    }
    return toModel(ra), nil
}

